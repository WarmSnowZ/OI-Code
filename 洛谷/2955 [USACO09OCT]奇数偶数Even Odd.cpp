#include <bits/stdc++.h>
#define ll long long
using namespace std;

int read() {
    int x = 0; bool m = 0; char c = getchar();
    while (!isdigit(c) && c != '-') c = getchar();
    if (c == '-') m = 1, c = getchar();
    while (isdigit(c)) x = x * 10 + c - '0', c = getchar();
    if (m) return -x; else return x;
}

int t, x;

int main() {
	
	t = read();
	while (t--) {
		x = read();
		if (x & 1) printf("odd\n");
		else printf("even\n");
	}
	
	return 0;
}

